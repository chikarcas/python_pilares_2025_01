def saludo(n):
    """Esta función imprime un mensaje saludando a la persona que se escriba como argumento de la función."""
    print("saludo " + n)
    return

def suma(a, b):
    """este funcion nos debuelve el resultado de dos numeros dados"""
    print(a + b)
    return

def desc(precio, descuento):
    """esta funcion nos calcula el precio con descuento de un articulo"""
    print(precio - (precio * (descuento / 100)))
    return

